public class VariableDataType {
    public static void main(String[] args) {
        int i = 10; //Khai bao bien i la kieu int co gia tri la 10
        float f = 20.5f; //Khai bao bien f co kieu la float co gia tri la 20.5
        double d = 20.5; //Khai bao bien d co kieu la double co gia tri la 20.5
        boolean b = true; //Khai bao bien b la kieu boolean co gia tri la true
        char c = 'a'; //Khai bao bien c co kieu la char co gia tri la ki tu a
        String s = "Hello"; //Khai bao bien s co kieu la String co gia tri la chuoi Hello

        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
